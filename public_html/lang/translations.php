function t($key, $lang = null) {
    global $translations;
    if (!$lang) $lang = CURRENT_LANG;
    return $translations[$lang][$key] ?? $translations[DEFAULT_LANGUAGE][$key] ?? $key;
}

function getLocalizedField($data, $field, $lang = null) {
    if (!$lang) $lang = CURRENT_LANG;
    $localizedField = $field . '_' . $lang;
    
    if (is_array($data)) {
        return $data[$localizedField] ?? $data[$field . '_' . DEFAULT_LANGUAGE] ?? '';
    } elseif (is_object($data)) {
        return $data->$localizedField ?? $data->{$field . '_' . DEFAULT_LANGUAGE} ?? '';
    }
    
    return '';
}
